public class Matematica {

            double n1, n2;


        public double soma(double n1, double n2){
            return n1 + n2;
        }

        public double sub(double n1, double n2){
            return n1 - n2;
        }

        public double div(double n1, double n2){
            return n1 / n2;
        }

        public double multi(double n1, double n2){
            return n1 * n2;
    }


}
